function changeLanguage(lang) {
  const elementsToTranslate = {
    l_language: 'language',
    l_product: 'product',
    l_winery: 'winery',
    l_origin_country: 'origin_country',
    l_terroir: 'terroir',
    l_grapes: 'grapes',
    l_vintage: 'vintage',
    l_sugar_content: 'sugar_content',
    l_alcohol: 'alcohol',
    l_net_quantity: 'net_quantity',
    l_lot: 'lot',
    l_nutrition_information: 'nutrition_information',
    l_type: 'type',
    l_per_100ml: 'per_100ml',
    l_energy: 'energy',
    l_fat: 'fat',
    l_saturates: 'saturates',
    l_carbohydrate: 'carbohydrate',
    l_sugars: 'sugars',
    l_protein: 'protein',
    l_salt: 'salt',
    l_ingredients: 'ingredients',
    l_allergens: 'allergens',
    l_additional_information: 'additional_information',
    l_viticulture: 'viticulture',
    l_winemaking: 'winemaking',
    l_aromas: 'aromas',
    l_taste: 'taste',
  };
  for (const id in elementsToTranslate) {
    const translationKey = elementsToTranslate[id];
    const element = document.getElementById(id);
    if (element) {
      element.textContent = translations[lang][translationKey] || element.textContent;
    }
  }
}

document.getElementById('elabel-lang__select').addEventListener('change', function() {
  const selectedLang = this.value;
  changeLanguage(selectedLang);
});
